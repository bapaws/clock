import HealthKit

public extension HKWorkoutActivityType {
    var name: String {
        if #available(iOS 16, *) {
            NSLocale.current.language.languageCode?.identifier == "zh" ? cnName : enName
        } else {
            // Fallback on earlier versions
            NSLocale.current.languageCode == "zh" ? cnName : enName
        }
    }

    /*
     Additional mapping for common name for activity types where appropriate.
     */
    var commonName: String {
        switch self {
        case .highIntensityIntervalTraining: return "HIIT"
        default: return name
        }
    }
}

